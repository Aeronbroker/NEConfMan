package eu.neclab.iotplatform.confman.couchdb.datamodel;

import javax.xml.bind.annotation.XmlAccessType;
import javax.xml.bind.annotation.XmlAccessorType;
import javax.xml.bind.annotation.XmlElement;
import javax.xml.bind.annotation.XmlRootElement;

import eu.neclab.iotplatform.confman.couchdb.CouchDB;
import eu.neclab.iotplatform.ngsi.api.datamodel.RegisterContextRequest;

@XmlRootElement(name = "registerContextUpdate")
@XmlAccessorType(XmlAccessType.FIELD)
public class RegisterContextUpdate extends RegisterContextRequest {

	@XmlElement(name = "_id", required = true)
	private String _id = null;
	@XmlElement(name = "_rev", required = true)
	private String _rev = null;
	
	
	
	public RegisterContextUpdate() {
		super();
	}

	public RegisterContextUpdate(RegisterContextRequest registerContextRequest) {
		super(registerContextRequest.getContextRegistrationList(), registerContextRequest.getDuration(), "");
		
		String registrationId = registerContextRequest.getRegistrationId();
		String[] idAndRev = registrationId.split(CouchDB.ID_REV_SEPARATOR);
		this._id = idAndRev[0];
		this._rev = idAndRev[1];
	}

	public String get_id() {
		return _id;
	}

	public void set_id(String _id) {
		this._id = _id;
	}

	public String get_rev() {
		return _rev;
	}

	public void set_rev(String _rev) {
		this._rev = _rev;
	}
	
	
	

}
